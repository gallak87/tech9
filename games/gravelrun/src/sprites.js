// GRAVELRUN — sprites.js
// Pixel art sprites as draw functions. No image loading, no async.
// Each sprite is defined as a 2D pixel grid (color index) + a palette.
// drawPixels renders the grid at (x,y) with pixel scale s.
//
// Palette (6 colors, indices 0-5):
//   0  transparent / skip
//   1  #0a0a0a  — background dark (deep shadow)
//   2  #3a3a3a  — concrete gray (dark armor, ground base)
//   3  #5a5a5a  — mid gray (armor mid-tone, ground surface)
//   4  #8a8a8a  — light gray / highlight (armor edge, details)
//   5  #b84a1a  — rust/accent (helmet, eye glow)
//   6  #d4a017  — bright accent (score pop, exit — very sparse)

const PAL = [
  null,       // 0 — transparent
  '#0a0a0a',  // 1 — deep shadow
  '#3a3a3a',  // 2 — concrete gray
  '#5a5a5a',  // 3 — mid gray
  '#8a8a8a',  // 4 — light gray / highlight
  '#b84a1a',  // 5 — rust / accent
  '#d4a017',  // 6 — bright accent
];

// drawPixels — render a 2D grid of palette indices onto ctx.
// grid: number[][]  (row-major, top to bottom)
// ox, oy: top-left canvas position
// s: pixel scale (each "pixel" is s×s canvas pixels)
// flipX: mirror horizontally around the sprite center
function drawPixels(ctx, grid, ox, oy, s, flipX = false) {
  const rows = grid.length;
  const cols = grid[0].length;
  ctx.save();
  if (flipX) {
    ctx.translate(ox + cols * s, oy);
    ctx.scale(-1, 1);
    ox = 0;
    oy = 0;
  }
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const idx = grid[r][c];
      if (!idx) continue; // 0 = transparent
      ctx.fillStyle = PAL[idx];
      const px = (flipX ? 0 : ox) + c * s;
      const py = (flipX ? 0 : oy) + r * s;
      ctx.fillRect(px, py, s, s);
    }
  }
  ctx.restore();
}

